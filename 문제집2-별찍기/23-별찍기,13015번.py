m = int(input())
print('*' * m + ' ' * (1 + (m - 2) * 2) + '*' * m)
for i in range(m-2):
    print(' ' * (i + 1) + '*' + ' ' * (m - 2) + '*' + ' ' * (2 * (m - 3 - i) + 1) + '*' + ' ' * (m - 2) + '*')
print(' ' * (m - 1) + '*' + ' ' * (m - 2) + '*' + ' ' * (m - 2) + '*')
for i in range(m-1, 1, -1):
    print(' ' * (i - 1) + '*' + ' ' * (m - 2) + '*' + ' ' * (2 * (m - i - 1) + 1) + '*' + ' ' * (m - 2) + '*')
print('*' * m + ' ' * ((m - 2) * 2 + 1) + '*' * m)
